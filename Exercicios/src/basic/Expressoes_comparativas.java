package basic;

public class Expressoes_comparativas {
    public static void main(String[] args) {

        // > maior.
        // < menor.
        // == igual.
        // ≥ maior, ou igual.
        // ≤ menor, ou igual.
        // != diferente.

        //EXEMPLOS

        int a = 44;
        int b = 22;
        int c = 44;

        System.out.println(a > b);//TRUE
        System.out.println(b > a);//FALSE
        System.out.println(a == c);//TRUE
        System.out.println(a >= b);//TRUE
        System.out.println(a <= b);//FALSE
        System.out.println(a != b);//TRUE

    }
}
